#!/bin/bash

DISK=`df -h / | awk '{print $2,$3,$4}'`
MEMORY=`free -m | awk '{print $1,$2,$3,$4}'`
CPU=`top | head -n 5`

echo -e "------------------------------------------------------------------------------------------------------------------------------------------------"
echo -e "Disk usage of your system : \n$DISK"
echo -e "------------------------------------------------------------------------------------------------------------------------------------------------"
echo -e "Status of your memory : \n$MEMORY"
echo -e "------------------------------------------------------------------------------------------------------------------------------------------------"
echo -e "Status of your central processor : \n$CPU"
echo -e "------------------------------------------------------------------------------------------------------------------------------------------------"
